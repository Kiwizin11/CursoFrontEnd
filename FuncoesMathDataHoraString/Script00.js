// Funções matematicas

// SQRT e POW (Quadrado e a Potencia)

// Calcular a raiz quadrada
console.log(Math.sqrt(25)); // = 5

// Calcular a potencia
console.log(Math.pow(7,2)); // = 7² = 49
console.log(Math.pow(4,3)); // = 4³ = 64
console.log(Math.pow(27,1/3)) // = ³√27 = 3


// funções de arredondamento (round, ceil, floor)

// Round
console.log(Math.round(4.7)); // = 5
console.log(Math.round(4.4)); // = 4

// Ceil
console.log(Math.ceil(4.1)); // = 5

// Floor
console.log(Math.floor(4.9)); // = 4


// Numeros aleatórios (Math.random)
console.log(Math.random()); //gera um número entre 0 e 1 (decimal)
console.log(Math.random()*1000); //gera um número entre 0 e 999

// valor absoluto
console.log(Math.abs(-10)); // = 10

//Minimos e máximos
console.log(Math.min(1,2,3,4,5,6,7,8,9)); // = 1
console.log(Math.abs(1,2,3,4,5,6,7,8,9)); // = 9
